package String;

public class StringDemo1 {

	public static void main(String[] args) 
	{
		String s1 = "     RBG Technologies    ";
		System.out.println(s1);
		
		String s2 = s1.trim();
		System.out.println(s2);
		
		String s3 = "Selenium";
		System.out.println(s3);
		
		System.out.println(s3.toUpperCase());
		

	}

}
